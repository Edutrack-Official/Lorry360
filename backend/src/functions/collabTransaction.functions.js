const { app } = require('@azure/functions');
const connectDB = require('../utils/db');
const {
  createTransaction,
  getTransactionsByCollaboration,
  getTransactionById,
  approveTransaction,
  rejectTransaction,
  markAsPaid,
  getCollabSummary,
  deleteTransaction
} = require('../controllers/collabTransaction.controller');
const { verifyToken } = require('../middleware/auth.middleware');

/**
 * ✅ Create Collab Transaction
 */
app.http('createCollabTransaction', {
  methods: ['POST'],
  authLevel: 'anonymous',
  route: 'collab-transactions/create',
  handler: async (request) => {
    try {
      await connectDB();
      
      const { decoded: user, newAccessToken } = await verifyToken(request);

      // Only owners can create transactions
      if (user.role !== 'owner') {
        return {
          status: 403,
          jsonBody: { success: false, error: 'Access denied. Owner role required.' },
        };
      }

      const body = await request.json();
      body.from_owner_id = user.userId; // Set creator from token

      const result = await createTransaction(body);
      
      const response = { status: 201, jsonBody: { success: true, data: result } };
      if (newAccessToken) {
        response.jsonBody.newAccessToken = newAccessToken;
      }
      
      return response;
    } catch (err) {
      return {
        status: err.status || 500,
        jsonBody: { success: false, error: err.message },
      };
    }
  },
});

